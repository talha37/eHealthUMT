For all older adults a regular program of strengthening, combined with aerobic exercises, can help reduce or prevent many functional declines associated with growing older.

Of 1,132 men and women who completed a South Shore YMCA basic fitness program, all showed significant benefits of strength and endurance training. All of the participants performed 25 minutes of strength exercise and 25 minutes of endurance exercise, two or three days per week for a period of eight weeks..</div>
